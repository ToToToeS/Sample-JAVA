public class If {
    public static void main(String [] args){
        int myInt = 10;
        if(myInt<10){
            System.out.println("Меньше 10");
        } else if (myInt> 10){
            System.out.println("Больше 10");
        }else{
            System.out.println("Равно 10");
        }
    }
}
